// Print the numbers from 1-5 to console using a loop

console.log("Using for loop");

for (let i = 1; i <= 5; i++) {
  console.log(i);
}

console.log("Using while loop");

let i = 1;
while (i <= 5) {
  console.log(i);
  i++;
}

console.log("Using do-while loop");

let j = 1;
do {
  console.log(j);
  j++;
} while (j <= 5);
